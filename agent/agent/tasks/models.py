# ---------------------------------------------------------------------------
# Request bodies for REST endpoints
# ---------------------------------------------------------------------------


class CreateTaskRequest(BaseModel):
    name: str
    prompt: str
    cron: str
    timezone: str = DEFAULT_TIMEZONE
    enabled: bool = True


class UpdateTaskRequest(BaseModel):
    name: str | None = None
    prompt: str | None = None
    cron: str | None = None
    enabled: bool | None = None
